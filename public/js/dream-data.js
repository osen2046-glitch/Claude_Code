// ── 虛詞移除（讓「過世的親人」能命中「過世親人」）──
function stripParticles(text) {
  return text.replace(/[的了在到著過很好有被]/g, '');
}

// ── 易經比對 ──
function matchHexagrams(dreamText) {
  const normalized = stripParticles(dreamText);
  const scores = {};
  hexagrams.forEach(hex => {
    let score = 0;
    const keywords = hex.dreamKeywords || [];
    keywords.forEach(kw => {
      if (dreamText.includes(kw) || normalized.includes(stripParticles(kw))) score++;
    });
    if (score > 0) scores[hex.id] = score;
  });
  // dreamThemes 跨域加分
  Object.values(dreamThemes).forEach(theme => {
    const matched = theme.keywords.some(kw =>
      dreamText.includes(kw) || normalized.includes(stripParticles(kw))
    );
    if (matched) {
      (theme.hexBonus || []).forEach(id => {
        scores[id] = (scores[id] || 0) + 2;
      });
    }
  });
  return Object.entries(scores)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 2)
    .map(([id]) => hexagrams.find(h => h.id === id));
}

