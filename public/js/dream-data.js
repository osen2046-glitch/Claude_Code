function getChakraResonance(archetypeIds) {
  if (!archetypeIds || archetypeIds.length === 0) return null;

  const chakraCount = {};
  archetypeIds.forEach(id => {
    const chakras = archetypeToChakra[id] || [];
    chakras.forEach(c => {
      chakraCount[c] = (chakraCount[c] || 0) + 1;
    });
  });

  const sorted = Object.entries(chakraCount)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 2)
    .map(([id]) => id);

  return sorted.map(id => {
    const card = chakraCards[id];
    const affirmation = card.affirmations[
      Math.floor(Math.random() * card.affirmations.length)
    ];
    return {
      chakra: card.chakra,
      color: card.color,
      colorName: card.colorName,
      keywords: card.keywords,
      affirmation
    };
  });
}
